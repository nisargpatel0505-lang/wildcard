class JokerDefinition {
  const JokerDefinition({
    required this.id,
    required this.name,
    required this.rarity,
    required this.description,
    required this.price,
    required this.unlock,
    required this.effect,
    this.starter = false,
    this.stateKey,
  });

  final String id;
  final String name;
  final JokerRarity rarity;
  final String description;
  final int price;
  final int unlock;
  final JokerEffect effect;
  final bool starter;
  final String? stateKey;

  int get collectionUnlockCost {
    if (unlock == 0) return 0;
    final multiplier = switch (rarity) {
      JokerRarity.common => 1.5,
      JokerRarity.uncommon => 2.2,
      JokerRarity.rare => 2.7,
      JokerRarity.wild => 3.0,
    };
    return ((unlock * multiplier) / 5).round() * 5;
  }

  int get startBoostPrice => switch (rarity) {
    JokerRarity.common => 6,
    JokerRarity.uncommon => 10,
    JokerRarity.rare => 16,
    JokerRarity.wild => 30,
  };
}

